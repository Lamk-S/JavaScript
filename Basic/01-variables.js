// var -> forma original para declarr variables, 
// se puede acceder a ellas sin aún haberlas declarado

var helloWorld = "¡Hola, Javascript!"
console.log(helloWorld)

helloWorld = "¡Hola de nuevo, Javascript!"
console.log(helloWorld)

// let -> No se puede acceder a ellas si aún no se las ha declarado

let = helloWorld2 = "¡Hola, Javascript! 2"
console.log(helloWorld2)

helloWorld2 = "¡Hola de nuevo, Javascript! 2"
console.log(helloWorld2)

// const -> Su valor es constante, una vez declarado no puede ser
// reasignado

const helloWorld3 = "¡Hola, Javascript! 3"
console.log(helloWorld3)

helloWorld3 = "¡Hola de nuevo, Javascript! 3"
console.log(helloWorld3)