// Declaramos una variable de tipo string
let text = "JavaScript"

// Mostramos el primer carácter
// Los strings comienzan en el índice 0
console.log(text[0])

// Mostramos el último carácter
// length - 1 siempre apunta al último índice
console.log(text[text.length - 1])